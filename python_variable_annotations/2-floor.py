#!/usr/bin/env python3
"""
Module qui fournit une fonction pour convertir un nombre
flottant en entier par troncature.
"""


def floor(n: float) -> int:
    """
    Convertit un nombre flottant en entier en le tronquant vers zéro.

    Args:
        n (float): Le nombre à convertir.

    Returns:
        int: La partie entière du nombre fourni.
    """
    return int(n)
