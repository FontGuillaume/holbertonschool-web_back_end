#!/usr/bin/env python3
"""
Module qui fournit une fonction pour convertir un
nombre flottant en chaîne de caractères.
"""


def to_str(n: float) -> str:
    """
    Convertit un nombre flottant en une chaîne de caractères.

    Args:
        n (float): Le nombre à convertir.

    Returns:
        str: La représentation en chaîne de caractères du nombre fourni.
    """
    return str(n)
